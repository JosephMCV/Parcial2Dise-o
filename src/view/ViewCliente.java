    private void updateTable() {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < model.getRowCount(); j++) {
                model.removeRow(j);
            }
        }
        for (int i = 0; i < clientes.size(); i++) {
            model.addRow(clientes.get(i));
        }
    }

