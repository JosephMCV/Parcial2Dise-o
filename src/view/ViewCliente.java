    private void JBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnGuardarActionPerformed
        // TODO add your handling code here:

        if (JTxtNombre.getText().isEmpty() || JTxtCliente.getText().isEmpty() || JTxtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int codigo = Integer.parseInt(JTxtCliente.getText());
        String nombre = JTxtNombre.getText();
        String email = JTxtEmail.getText();
        models.Clientes cliente = new models.Clientes(codigo, nombre, email);
        controller.insertCliente(cliente);
        clientes = controller.searchClientes(-1);
        updateTable();


    }//GEN-LAST:event_JBtnGuardarActionPerformed

    private void JBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnEditarActionPerformed
        // TODO add your handling code here:
        if (JTxtNombre.getText().isEmpty() || JTxtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (JTxtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el codigo de la categoria a eliminar en la barra de busqueda", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int id = Integer.parseInt(JTxtCliente.getText());
        String nombre = JTxtNombre.getText();
        String email = JTxtEmail.getText();
        models.Clientes cliente = new models.Clientes(id, nombre, email);
        controller.updateCliente(cliente);
        clientes = controller.searchClientes(-1);
        updateTable();
    }//GEN-LAST:event_JBtnEditarActionPerformed

    private void JBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnEliminarActionPerformed
        // TODO add your handling code here:
        if (JTxtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el codigo de la categoria a eliminar en la barra de busqueda", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int id = Integer.parseInt(JTxtBuscar.getText());
        controller.deleteCliente(id);
        clientes = controller.searchClientes(-1);
        updateTable();
    }//GEN-LAST:event_JBtnEliminarActionPerformed

    private void JBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnBuscarActionPerformed
        // TODO add your handling code here:
        int code;
        if (JTxtBuscar.getText().isEmpty()) {
            code = -1;
        } else {
            code = Integer.parseInt(JTxtBuscar.getText());
        }
        clientes = controller.searchClientes(code);
        updateTable();
    }//GEN-LAST:event_JBtnBuscarActionPerformed

